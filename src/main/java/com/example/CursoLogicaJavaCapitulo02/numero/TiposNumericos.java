package com.example.CursoLogicaJavaCapitulo02.numero;

import java.util.Scanner;

public class TiposNumericos {

    public static void main(String[] args) {

        Byte numeroByte = 127; //Sua capacidade de almacenamento é entre -128 até 127

        Short numeroShort = -32768; //Sua capacidade de almacenamento é entre -32768 até 32767

        Integer numeroInteger = 2147483647; //Sua capacidade de almacenamento é entre -2147483648 até 2147483647

        Long numeroLong = 1L; //Sua capacidade de almacenamento é maior que o Integer

        Float numeroFloat = 1.0f; // O float só tem 7 digitos decimais de precisão

        Double numeroDouble = 1.0; // O double tem 15 digitos decimais de precisão

    }

}
